package com.bookkeepo.accounting.model;

/**
 * @author Yash Singh
 *
 */
public class LedgerColumns {

	String particulars;
	
	String date;
	
	String debit;
	
	String credit;
	
	String debitSum;
	
	String creditSum;
	
	String balance;

	/**
	 * @return the particulars
	 */
	public String getParticulars() {
		return particulars;
	}

	/**
	 * @param particulars the particulars to set
	 */
	public void setParticulars(String particulars) {
		this.particulars = particulars;
	}

	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}

	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}

	/**
	 * @return the debit
	 */
	public String getDebit() {
		return debit;
	}

	/**
	 * @param debit the debit to set
	 */
	public void setDebit(String debit) {
		this.debit = debit;
	}

	/**
	 * @return the credit
	 */
	public String getCredit() {
		return credit;
	}

	/**
	 * @param credit the credit to set
	 */
	public void setCredit(String credit) {
		this.credit = credit;
	}

	/**
	 * @return the balance
	 */
	public String getBalance() {
		return balance;
	}

	/**
	 * @param balance the balance to set
	 */
	public void setBalance(String balance) {
		this.balance = balance;
	}

	/**
	 * @return the creditSum
	 */
	public String getCreditSum() {
		return creditSum;
	}

	/**
	 * @param creditSum the creditSum to set
	 */
	public void setCreditSum(String creditSum) {
		this.creditSum = creditSum;
	}

	/**
	 * @return the debitSum
	 */
	public String getDebitSum() {
		return debitSum;
	}

	/**
	 * @param debitSum the debitSum to set
	 */
	public void setDebitSum(String debitSum) {
		this.debitSum = debitSum;
	}
	
	
}
